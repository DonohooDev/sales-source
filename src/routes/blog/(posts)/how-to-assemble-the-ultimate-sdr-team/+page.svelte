        <section id="mark-pises-the-writer">
            <h3>Mark Pises, The Writer</h3>
            <p>
                SDR organizations cannot succeed without effective written communication that
                inspires interest and action. While SDRs should not be left to invent their own
                email & social messaging, it’s critical that you have great writers on the team who
                can collaborate with product marketing and sales management on what resonates with
                buyers.
            </p>
            <p>
                Much like myself, Mark Pises decided that there were enough lawyers in the world and
                recused himself from the legal path. However, he kept the writing skills and
                attention to detail that make a great lawyer (and a great sales rep). I was blown
                away when I discovered that Mark developed an outbound sequence specifically for
                marketers in the non-profit space. He had figured out how to shape the language of
                digital marketing away from “prospects” and “conversions” to “donors” and
                “constituent engagement.” That’s the kind of laboratory of experimentation that
                keeps your messaging sharp and relevant.
            </p>
            <p>
                Mark has since moved up at Bluewolf, applying his thoughtful customer approach to
                enterprise organizations as an Account Executive in the field.
            </p>
        </section>

        <section id="amy-heng-the-analyst">
            <h3>Amy Heng, The Analyst</h3>

            <p>
                I strongly feel that great SDR leaders need to have their arms around operational
                efficiency (or a close ops partner to do this heavy lifting). Second only to growth
                marketing, sales development is the revenue function that relies most on “tuning the
                engine.” Every inefficiency in an SDR’s daily process quickly multiplies across
                activities to cause a considerable slowdown in productivity. Guess who senses those
                inefficiencies better than anyone? Yep, the SDR.
            </p>
            <p>
                That’s one of the reasons I hired Amy Heng. Not only was she fearless and motivated,
                she demonstrated strong analytical aptitude that I knew would be valuable to the
                entire team. Lucky for me, Amy was able to exceed her personal goals while making
                time for value-added projects. In one such project, Amy carefully timed the triaging
                process for every inbound lead across a few members of the team. She found that reps
                were spending 5-8 minutes just doing diligence around existing accounts in the CRM.
                Multiply this by 40 leads/day and 8 reps, and we had a 42 hour/day problem. Good
                thing there was a perfect solution (hint: LeanData).
            </p>
            <p>
                I’m glad to report that Amy is now employing those same analytical skills as a
                Marketing Manager at Palo Alto Networks.
            </p>
        </section>

        <section id="erin-reynolds-the-energizer">
            <h3>Erin Reynolds, The Energizer</h3>

            <p>
                What job is harder than calling busy strangers all day and convincing them to take a
                meeting with your company? A well-positioned cheerleader, hype man/woman, or
                superfan can act as a force multiplier for the energy and well being of the entire
                team.
            </p>
            <p>
                Nobody on my team embodied this role better than Erin Reynolds. One of my favorite
                examples was a team competition that Erin designed to drive qualified appointments
                at Dreamforce. She drew a massive dragon on one of our whiteboard walls, checkered
                it into dozens of boxes, and threw down the gauntlet for the team to collectively
                “slay the dragon.” For each appointment booked, a member of the team colored in one
                of the boxes and labeled it with the name of the target account. Needless to say,
                the team absolutely obliterated the dragon and enjoyed the cash bonuses that
                followed.
            </p>
            <p>
                It’s no surprise that Erin is now energizing an entire ecosystem as a Channel
                Partnerships Manager at MessageMedia.
            </p>
        </section>

        <section id="mac-cady-the-talker">
            <h3>Mac Cady, The Talker</h3>
            <p>
                The phone is not dead; it’s just become less forgiving. SDRs better serve up a damn
                good message with immediate relevance if they catch a decision maker live on the
                phone. While every SDR you hire needs to have fluency in the phone channel, it’s a
                massive boon to fill the ranks with a handful of true conversational pros. These
                people will provide real-time coaching to peers when they overhear a conversation
                that fell short of converting. And they will fill out your “best calls” recording
                library with invaluable examples for future hires.
            </p>
            <p>
                When I interviewed Mac Cady and most of his answers involved “wouldn’t it be easier
                to just pick up the phone and call them,” I knew I had found one such conversational
                powerhouse. Mac went on to become the top performing SDR in his hiring cohort, but
                importantly he made others better while doing it. I actually worked with Mac to
                develop the call review scorecard that we implemented for 1:1s with the rest of the
                team. I now use a similar version of that scorecard at SalesSource when I’m
                consulting with clients.
            </p>
            <p>
                I’m proud to say that Mac pursued the path of SDR leadership and is now the Senior
                Manager, Inside Sales & Account Development at Mesosphere.
            </p>
            <p>What other profiles do you look for when hiring SDRs?</p>
