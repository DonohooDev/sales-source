    <h1 id="intro" slot="title">
        Unlocking Success with Outsourced Sales Development: Key Insights for SaaS Companies
    </h1>

    <span slot="intro">
        <p>
            In the last few years, we’ve seen a wave of outsourced sales development representative <abbr
                >(SDR)</abbr
            >
            vendors introduced to the SaaS community. An outsourced
            <abbr title="Sales Development Representative">SDR</abbr> firm is a company that owns appointment
            setting end-to-end, typically conducting outbound outreach, qualifying inbound leads, or
            a combination of both. There are generally three types of outsourced model: pay per appointment,
            pay for activities and a retainer model (no promises but they’ll do their best).
        </p>
        <p>
            While we do have our opinions, we thought it would be best to first take a step back and
            look at this emerging trend from a higher view. Before we talk about which vendor to
            use, why are you considering this as an option for your company? Is this the best path?
            What should you know before you start taking this option seriously?
        </p>
    </span>

    <div slot="content">
        <section>
            <h3 id="outsourced-firms">
                Where were outsourced firms a few years ago, where are they now, and where are they
                headed?
            </h3>
            <p>
                <b>5 Years Ago –</b> Traditional outsourced companies were not using modern best practices.
                Cold calling was executed with general scripts and little qualification. Marketing blasts
                were sent via email with tools like Marketo, Eloqua and Pardot. Outsourced firms were
                more like marketers rather than performing actual sales development work.
            </p>
            <p>
                <b>Now –</b> With the boom of sequencers and automation technologies, outsourced teams
                can get closer to what an in-house team can do. Now firms deploy best-in-class tech stacks
                including third party data providers, sales engagement platforms, multi-channel prospecting
                tools, and comprehensive research solutions.
            </p>
            <p>
                <b>5 Years From Now –</b> Software will start to eat farther down the qualification funnel
                and allow outsourced firms to get more specialized. Logic will be built in to the point
                where AI can handle more prospect engagement. Reps can have more creative time by doing
                things like researching and creating customized account prospecting strategies.
            </p>
        </section>

        <section>
            <h3 id="typical-scenarios">
                What are typical scenarios where companies go outsourced?
            </h3>
            <p>
                Companies at every stage ask us if they should consider working with outsourced <abbr
                    title="Sales Development Representative">SDR</abbr
                >
                firms. After all, it’s risky to refocus full-time resources on an unproven strategy –
                <i>when that strategy involves salespeople who</i>
                <i>are not given a revenue/bookings quota. </i>Before we discuss the pros and cons
                of adopting an outsourced model, let’s talk about when it might make sense to
                outsource <abbr title="Sales Development Representative">SDR</abbr> work:
            </p>
            <p>
                <b>Proving the <abbr title="Sales Development Representative">SDR</abbr> model –</b>
                Outsourcing sales development for the first time is less daunting, less expensive and
                can be easily folded or redirected. On the other hand, if you adopt an outsourced model
                that is not properly executed, you may assume that sales development is not a fit for
                your business (when it was really just an under performing vendor).
            </p>
            <p>
                <b>Expanding into a new market –</b> Sales development teams provide an incredible feedback
                loop from potential customers in your market (how they react to your value prop, what’s
                their current solution, what questions they ask about your solution). New markets can
                be incredibly complex to test, validate and navigate. Not including the fact that it’s
                a heavy investment when done in-house.
            </p>
            <p>
                <b
                    >Offloading tactical <abbr title="Sales Development Representative">SDR</abbr> work
                    –
                </b>Having an option to allow your team to focus on pure selling activities is the
                ultimate goal. We are seeing more companies partner with outsourced firms to lift
                tactical tasks from their
                <abbr title="Sales Development Representative">SDRs</abbr>. For example, outsourced
                teams could take on the task of human account research and feeding target contacts
                into your CRM.
            </p>
            <p>
                <b>Augmenting what’s working internally –</b> Imagine you are a sales leader
                carrying a much bigger number without the budget to hire enough full-time headcount.
                You might have enough money to augment your
                <abbr title="Sales Development Representative">SDR</abbr>
                team with a squad of <abbr title="Sales Development Representative">SDRs</abbr> “on tap.”
            </p>
            <p>
                <b>Companies with marketing departments that need to engage with non-TAM leads –</b>
                As companies grow, their internal
                <abbr title="Sales Development Representative">SDRs</abbr> become more strategic and
                account driven. It’s typically not a good use of their time to focus on companies that
                fall outside of the Target Addressable Market (TAM). However, these companies could still
                generate revenue – we’ve seen clients go outsourced in this scenario.
            </p>
        </section>

        <section>
            <h3 id="outsourced-pros">Pros of Going Outsourced</h3>
            <p>
                <b>Money and Time – </b>You don’t have to spend money on recruiting, benefits, risk
                of mis-hiring, cost of ramping reps, promoting, and management costs that go along
                with scale.
            </p>
            <p>
                <b>Tools and Systems Access –</b> Whether it be sales engagement platforms or data
                enrichment tools, providers generally have access to best of breed tools and have
                strategies that are tested from previous. Going outsourced saves you from the
                significant recurring cost and technical debt of building your own
                <abbr title="Sales Development Representative">SDR</abbr> tech stack.
            </p>
            <p>
                <b>Scalable –</b> If your outsourced
                <abbr title="Sales Development Representative">SDR</abbr> experience is a success, outsourced
                firms generally don’t have a problem scaling their efforts. This ties in closely with
                the benefit of avoiding recruiting and ramping headaches.
            </p>
            <p>
                <b>Experience</b> – Outsourced firms have the benefit of running dozens of experiments
                in parallel to see what works and what doesn’t – in theory making them more effective.
                They can also apply the strategies and best practices that you may not have in-house.
            </p>
        </section>

        <section>
            <h3 id="outsourced-cons">Cons of Going Outsourced</h3>
            <p>
                <b>TAM and ICP –</b> If you don’t have your Target Addressable Market (TAM) and
                Ideal Customer Profile (ICP) clearly defined and communicated, you will hinder your
                outsourced
                <abbr title="Sales Development Representative">SDRs</abbr> ability to perform. Meetings
                will get booked, but pipeline, bookings, and churn metrics will suffer.
            </p>
            <p>
                <b>Disconnect with internal leadership and team –</b> While you receive regular
                updates, it can be challenging to get a feel of how your outsourced
                <abbr title="Sales Development Representative">SDRs</abbr> are doing or performing at
                any given time. You will get regular reports but making/suggesting changes can be challenging
                in real time.
            </p>
            <p>
                <b>Vetted best practices –</b> If your chosen outsourced firm is not using best practices,
                they will burn through your lead lists quickly and diminish your brand. If they say they
                are using tested best practices, don’t take their word for it. Ask for sample messages,
                cadence examples, scripts, and previous customer references.
            </p>
            <p>
                <b>Lose learnings –</b> A little acknowledged fact is that
                <abbr title="Sales Development Representative">SDRs</abbr>
                can be a critical part of the feedback loop for the product team. If
                <abbr title="Sales Development Representative">SDRs</abbr> are regularly hearing from
                prospects that they’d like a Gmail integration, this information is gold. While outsourced
                firms say they provide this type of information to their clients, they’re not as incentivized
                to do so and we have seen this information fall through the cracks.
            </p>
            <p>
                <b>Developing in-house talent –</b> If you agree with the idea that the best talent
                a company can have is developed in-house, then you’re at a huge disadvantage when
                going outsourced. When you think about developing your next best field reps,
                customer success reps, marketers and leadership, there’s no better option than
                in-house <abbr title="Sales Development Representative">SDRs</abbr>.
            </p>
        </section>

        <section>
            <h3 id="summary">In Summary</h3>
            <p>
                While Outsourced <abbr title="Sales Development Representative">SDR</abbr> firms
                have promise, they are still finding their identity. Are they a silver bullet to
                pipeline generation? Certainly not. But outsourced firms <i>can</i> add value when they
                are implemented in the right context.
            </p>
            <p>
                Successful early adopters understand the drawbacks of outsourcing and realize that
                they offer a supplemental strategy. If you want to optimize for the success of an
                outsourced team, ensure you institute clear and constant communication, do
                everything in your power to enable them and above all, value the learnings rather
                than the outcome.
            </p>
            <p>
                If you do go with an outsourced vendor, make sure that you get access to the
                “exhaust” that comes from all of the false positives the firm collects. It’s as
                important to make sure you get the information of where a prospect did not want to
                continue the conversation or process. This will allow you to update your systems so
                unfit prospects will no longer be reached out to, saving you and your team valuable
                time.
            </p>
            <p>
                While you can be successful if you follow the guidelines we’ve listed here, we still
                recommend an in-house sales development team if you want to drive meaningful growth
                over the long run.
            </p>
        </section>

